    private void btnCekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCekActionPerformed
        // TODO add your handling code here:
        harga=Integer.parseInt(txtHarga.getText());
        uangmuka=Integer.parseInt(txtUangmuka.getText());
        sisa=harga-uangmuka;
        txtSisa.setText(String.valueOf(sisa));
        
        lama=String.valueOf(cmbLama.getSelectedItem());
        harga=Integer.parseInt(String.valueOf(txtHarga.getText()));
        if (lama == "12") {
            cicilan=harga/12;
        } 
        else if (lama=="24") {
            cicilan=harga/24;
        }
        else if (lama=="36") {
            cicilan=harga/36;
        }
        txtCicilanperbulan.setText(String.valueOf(cicilan));
        
        if (sisa > 0) {
            keterangan ="belum lunas";
        } else {
            keterangan="lunas";
        }
        txtStatusketerangan.setText(keterangan);
    }//GEN-LAST:event_btnCekActionPerformed


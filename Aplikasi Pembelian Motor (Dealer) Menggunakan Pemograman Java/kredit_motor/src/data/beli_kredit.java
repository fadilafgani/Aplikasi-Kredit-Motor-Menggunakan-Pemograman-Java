    private void tampildata(String aql) {
        DefaultTableModel datalist = new DefaultTableModel();
        datalist.addColumn("No");
        datalist.addColumn("Kode Kredit");
        datalist.addColumn("Tanggal Pengkreditan");
        datalist.addColumn("Kode Motor");
        datalist.addColumn("Kode Pembeli");
        datalist.addColumn("Uang Muka");
        datalist.addColumn("Lama cicilan");
        datalist.addColumn("Cicilan");
        datalist.addColumn("Sisa");
        datalist.addColumn("Keterangan");
        try {
            int i = 1;
            st=con.createStatement();
            rsbrg=st.executeQuery("select * from beli_kredit");
            while (rsbrg.next()) {
                datalist.addRow(new Object[]{
                    (""+i++),rsbrg.getString(1),
                    rsbrg.getString(2),rsbrg.getString(3),
                    rsbrg.getString(4),rsbrg.getString(5),
                    rsbrg.getString(6),rsbrg.getString(7),
                    rsbrg.getString(8),rsbrg.getString(9)
            });
            gridBarang.setModel(datalist);
            }
          } catch (Exception e) {
               JOptionPane.showMessageDialog(null, "Gagal tampil \n"+e.getMessage());
        }
    }
